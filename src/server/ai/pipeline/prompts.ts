export function buildAgentContext(agentId: AgentId, input: string, previousOutputs: Partial<Record<AgentId, string>>) {
  const prev = Object.entries(previousOutputs)
    .map(([id, output]) => `## مخرجات ${id}\n${output}`)
    .join("\n\n---\n\n");

  if (agentId === "orchestrator") {
    return `الطلب الأصلي:\n${input}\n\nصنّف الطلب، حدّد زوايا التعمق، واقترح مسار التعلم للوكيل الأخير.`;
  }

  if (agentId === "exercise_gen") {
    return `الطلب الأصلي:\n${input}\n\nمخرجات الفريق كاملة:\n${prev}\n\nأنتج "نتيجة الفريق" بالأقسام الخمسة: فهم الموضوع / تحليل الموضوع / تطبيق الموضوع / اختبر نفسك / ماذا تتعلم بعد ذلك؟`;
  }

  if (agentId === "writer") {
    return `الطلب الأصلي:\n${input}\n\nالمخرجات السابقة:\n${prev}\n\nاكتب النتيجة النهائية المتماسكة للمستخدم.`;
  }

  return `الطلب الأصلي:\n${input}\n\nالمخرجات السابقة:\n${prev || "لا توجد بعد."}\n\nأنجز دورك المحدد فقط.`;
}
