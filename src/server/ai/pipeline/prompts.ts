function formatCurriculumContext(curriculum: CurriculumGrounding): string {
  const lessons = curriculum.matches.length
    ? curriculum.matches.map((lesson, index) => [
        `درس ${index + 1}: ${lesson.title}`,
        `الدورة: ${lesson.cycle ?? "غير محددة"}`,
        `المستوى: ${lesson.grade ?? "غير محدد"}`,
        `المادة: ${lesson.subject ?? "غير محددة"}`,
        `الوحدة: ${lesson.unit ?? "غير محددة"}`,
        lesson.objectives?.length ? `الأهداف: ${lesson.objectives.join("؛ ")}` : "",
        lesson.contentSnippet ? `مقتطف المحتوى: ${lesson.contentSnippet}` : "",
        lesson.exerciseCount != null ? `عدد التمارين المتاحة: ${lesson.exerciseCount}` : "",
      ].filter(Boolean).join("\n")).join("\n\n")
    : "لا توجد دروس مطابقة مؤكدة في قاعدة البيانات. استعمل وضع aligned_fallback فقط إذا كانت المادة والموضوع واضحين.";

  return [
    "## السياق المنهجي من OQUL",
    `الوضع: ${curriculum.mode}`,
    `قوة السياق: ${curriculum.strength}`,
    `الثقة: ${curriculum.confidence}`,
    `نوع الطلب: ${curriculum.intent}`,
    `لغة التدريس: ${curriculum.languageOfInstruction}`,
    `الدورة: ${curriculum.cycle ?? "غير محددة"}`,
    `المستوى/الدرجة: ${curriculum.grade ?? "غير محدد"}`,
    `المادة: ${curriculum.subject ?? "غير محددة"}`,
    `الوحدة: ${curriculum.unit ?? "غير محددة"}`,
    `الدرس: ${curriculum.lesson ?? "غير محفوظ في قاعدة OQUL"}`,
    `المحور/الموضوع: ${curriculum.topic ?? "غير محدد"}`,
    `سياق الامتحان: ${curriculum.examContext}`,
    curriculum.studentFacingNotice ? `تنبيه للطالب: ${curriculum.studentFacingNotice}` : "",
    curriculum.warnings.length ? `تنبيهات: ${curriculum.warnings.join(" | ")}` : "",
    "",
    "## الدروس المطابقة",
    lessons,
  ].filter(Boolean).join("\n");
}

export function buildAgentContext(
  agentId: AgentId,
  input: string,
  previousOutputs: Partial<Record<AgentId, string>>,
  curriculum: CurriculumGrounding,
) {
  const prev = Object.entries(previousOutputs)
    .map(([id, output]) => `## مخرجات ${id}\n${output}`)
    .join("\n\n---\n\n");
  const curriculumContext = formatCurriculumContext(curriculum);

  if (agentId === "orchestrator") {
    return `${curriculumContext}

## الطلب الأصلي
${input}

صنف الطلب، حدد المادة والمستوى والمحور، ثم ضع نطاقًا آمنًا للفريق. إذا كان الوضع aligned_fallback فاشرح أنه دعم متوافق مع توقعات المنهاج المغربي وليس درسًا محفوظًا رسميًا.`;
  }

  if (agentId === "exercise_gen") {
    return `${curriculumContext}

## الطلب الأصلي
${input}

## مخرجات الفريق كاملة
${prev}

أنتج النتيجة النهائية حسب نوع الطلب. في lesson: شرح وأمثلة وتمارين قصيرة مع تصحيح. في exam: تمارين امتحانية، تصحيح كامل، توزيع نقط، انتظارات المصحح، أخطاء شائعة، ونصيحة زمنية. التزم باللغة والمادة المحددتين.`;
  }

  if (agentId === "writer") {
    return `${curriculumContext}

## الطلب الأصلي
${input}

## المخرجات السابقة
${prev}

اكتب النتيجة النهائية المتماسكة دون الخروج عن السياق المنهجي. إذا كان الوضع aligned_fallback، ابدأ بتنبيه لطيف للطالب كما هو مذكور في السياق.`;
  }

  return `${curriculumContext}

## الطلب الأصلي
${input}

## المخرجات السابقة
${prev || "لا توجد بعد."}

أنجز دورك المحدد فقط مع الالتزام بالسياق المنهجي والمادة المختارة.`;
}
