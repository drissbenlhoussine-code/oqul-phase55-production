await withDb(async (client) => {
  const selfScore = scoreLessonQuality({
    lesson_title: PAYLOAD.title,
    explanation: PAYLOAD.explanation,
    summary: PAYLOAD.summary,
    vocabulary: PAYLOAD.vocabulary,
    examples: PAYLOAD.examples,
    exercise_count: PAYLOAD.exercises.length,
  });
  console.log(`\nSelf-score: ${selfScore.score}/100 — ${selfScore.issues.length === 0 ? "✅ No issues" : "⚠️ Issues: " + selfScore.issues.join(", ")}`);
  if (selfScore.score < 85) { console.error("Quality gate failed."); process.exit(1); }

  const matches = await fetchLessons(client, {
    grade: "2ap",
    subject: "اللغة العربية",
    title: "القراءة",
  });

  console.log(`\nMatches found: ${matches.length}`);
  if (matches.length) console.table(matches.map(r => ({ id: r.lesson_id, title: r.lesson_title, unit: r.unit_title })));

  const lesson = matches.find(r =>
    (r.lesson_title ?? "").includes("القراءة") &&
    (r.lesson_title ?? "").includes("أفهم") &&
    (r.lesson_title ?? "").includes("السنة الثانية")
  );

  if (!lesson) { console.error("❌ Lesson not found. Check seed or title."); process.exit(1); }

  printLessonSummary("BEFORE", lesson);
  console.log(`\nPayload: ${PAYLOAD.explanation.length} chars | ${PAYLOAD.exercises.length} exercises`);

  if (!apply) { console.log("\n✅ Dry run complete. No changes written."); return; }

  const counts = await updateLessonWithPayload(client, lesson.lesson_id, PAYLOAD, { replaceExercises: true });
  console.log("\nUpdate counts:", counts);

  const [verified] = await fetchLessons(client, { lessonId: lesson.lesson_id, limit: 1 });
  printLessonSummary("AFTER", verified);
});
